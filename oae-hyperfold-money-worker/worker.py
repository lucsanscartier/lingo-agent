from __future__ import annotations

import argparse
import datetime as dt
import hashlib
import json
import os
import sys
from pathlib import Path
from typing import Any, Dict, List

ARTIFACT_DIR = Path(os.getenv("OAE_ARTIFACT_DIR", "artifacts"))
LEDGER_PATH = Path(os.getenv("OAE_LEDGER_PATH", "ledger.jsonl"))
STRIPE_PAYMENT_LINK = os.getenv("STRIPE_PAYMENT_LINK", "https://buy.stripe.com/00wfZh3K1gWpgkVgzs8k80e")
STRIPE_PAYMENT_LINK_ID = os.getenv("STRIPE_PAYMENT_LINK_ID", "plink_1Tbq2nFRuWOMBXuRWGwCmhIg")
PRODUCT_NAME = os.getenv("OAE_PRODUCT_NAME", "ARCH Money Agent Starter Report")
PRICE_CAD = int(os.getenv("OAE_PRICE_CAD", "19"))


def utc_now() -> str:
    return dt.datetime.now(dt.UTC).isoformat()


def stable_hash(obj: Any) -> str:
    return hashlib.sha256(json.dumps(obj, sort_keys=True, ensure_ascii=False).encode("utf-8")).hexdigest()


def ensure_dirs() -> None:
    ARTIFACT_DIR.mkdir(parents=True, exist_ok=True)


def evidence_block(extra: List[str] | None = None) -> str:
    labels = [
        "VERIFIED: Artifact generated by OAE Hyperfold Money Worker v0.1.",
        "VERIFIED: Product route points to a Stripe payment link.",
        "INFERRED: Recommendations are generated from supplied input and reusable business logic.",
        "SPECULATIVE: Hyperfold Compute is an architecture metaphor for routing, memory, and cost compression; not verified new physics.",
        "LIMITATION: This report is informational and does not guarantee revenue.",
    ]
    if extra:
        labels.extend(extra)
    return "\n".join(f"- {x}" for x in labels)



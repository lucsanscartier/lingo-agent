def write_artifact(job: Dict[str, Any], markdown: str) -> Dict[str, str]:
    ensure_dirs()
    job_id = str(job.get("job_id", "job"))
    safe_id = "".join(ch if ch.isalnum() or ch in "-_" else "-" for ch in job_id)[:80]
    digest = stable_hash({"job": job, "markdown": markdown})[:12]
    md_path = ARTIFACT_DIR / f"{safe_id}-{digest}.md"
    json_path = ARTIFACT_DIR / f"{safe_id}-{digest}.json"

    artifact_record = {
        "job": job,
        "artifact_path": str(md_path),
        "artifact_sha256": stable_hash(markdown),
        "generated_at": utc_now(),
        "worker": "oae-hyperfold-money-worker-v0.1",
    }

    md_path.write_text(markdown, encoding="utf-8")
    json_path.write_text(json.dumps(artifact_record, indent=2, ensure_ascii=False), encoding="utf-8")
    append_ledger(artifact_record)
    return {"markdown": str(md_path), "json": str(json_path)}


def append_ledger(record: Dict[str, Any]) -> None:
    LEDGER_PATH.parent.mkdir(parents=True, exist_ok=True) if LEDGER_PATH.parent != Path('.') else None
    with LEDGER_PATH.open("a", encoding="utf-8") as f:
        f.write(json.dumps(record, ensure_ascii=False) + "\n")


def load_queue(path: str) -> List[Dict[str, Any]]:
    data = json.loads(Path(path).read_text(encoding="utf-8"))
    if not isinstance(data, list):
        raise ValueError("Queue must be a JSON list of jobs")
    return data


def process_job(job: Dict[str, Any]) -> Dict[str, Any]:
    if not job.get("paid"):
        return {"job_id": job.get("job_id"), "status": "skipped", "reason": "job is not marked paid"}
    artifact_type = job.get("product", "HF Opportunity Report").lower()
    if "opportunity" in artifact_type or "report" in artifact_type:
        markdown = generate_hf_opportunity_report(job)
    else:
        markdown = generate_hf_opportunity_report(job)
    paths = write_artifact(job, markdown)
    return {"job_id": job.get("job_id"), "status": "done", "paths": paths}


def run_probe() -> None:
    demo_job = {
        "job_id": "probe-001",
        "paid": True,
        "product": "HF Opportunity Report",
        "price_cad": PRICE_CAD,
        "customer_email": "probe@example.com",
        "input": {
            "idea": "Use OAE Hyperfold Compute to sell finished AI artifacts with cheap authorized compute.",
            "target_customer": "agents, builders, small businesses, and solo founders",
            "budget": "free/cheap authorized compute only",
            "tone": "clear, practical, energetic",
        },
    }
    result = process_job(demo_job)
    print(json.dumps({"mode": "probe", "result": result}, indent=2))


def run_queue(queue_path: str) -> None:
    jobs = load_queue(queue_path)
    results = [process_job(job) for job in jobs]
    print(json.dumps({"mode": "queue", "results": results}, indent=2))


def run_stripe_placeholder() -> None:
    # This is intentionally a placeholder until STRIPE_SECRET_KEY is available to the HF job.
    # When enabled, this should poll completed Checkout Sessions or receive webhook-created queue jobs.
    if not os.getenv("STRIPE_SECRET_KEY"):
        print(json.dumps({
            "mode": "stripe",
            "status": "blocked",
            "reason": "STRIPE_SECRET_KEY is not available inside this worker environment",
            "payment_link_id": STRIPE_PAYMENT_LINK_ID,
            "next_step": "Add a Stripe API key/webhook bridge or use ChatGPT connector as control-plane until then."
        }, indent=2))
        return
    print(json.dumps({
        "mode": "stripe",
        "status": "not_implemented_yet",
        "reason": "Stripe secret detected, but polling/webhook queue implementation is next module."
    }, indent=2))


def main() -> None:
    parser = argparse.ArgumentParser()
    parser.add_argument("--mode", choices=["probe", "queue", "stripe"], default="probe")
    parser.add_argument("--queue", default="queue.example.json")
    args = parser.parse_args()

    if args.mode == "probe":
        run_probe()
    elif args.mode == "queue":
        run_queue(args.queue)
    elif args.mode == "stripe":
        run_stripe_placeholder()


if __name__ == "__main__":
    main()
